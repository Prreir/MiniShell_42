#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*s1;
	int		i;

	i = 0;
	s1 = malloc(ft_strlen(str) + 1);
	if (!s1)
		return (0);
	while (str[i] != '\0')
	{
		s1[i] = str[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
